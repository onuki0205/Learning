// 掛け算メソッドを作成してみよう

public class Main {
    public static void main(String[] args) {
        System.out.println(multi(3, 4));
        System.out.println(multi(5, 7));
        System.out.println(multi(12, 34));
    }

    public static int multi(int x, int y) {
        // この下に処理を記述する
        return x * y;
    }
}

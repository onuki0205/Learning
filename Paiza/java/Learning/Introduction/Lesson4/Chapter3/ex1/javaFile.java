/**
5つの要素を持つ配列が定義されています。
この配列の最初の要素(インデックスが0の要素)をprintlnメソッドで出力してください。

**/

// 配列から特定要素を取り出す

public class Main {
    public static void main(String[] args) {
        String[] team = {"勇者", "戦士", "侍", "忍者", "魔法使い"};
        // teamの1番左の要素をprintlnメソッドで出力する
        System.out.println(team[0]);
    }
}
